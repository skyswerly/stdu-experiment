/**
 * @file    hal.h
 * @brief   硬件抽象层 (Hardware Abstraction Layer)
 *
 * 定义全项目共享的基础数据结构和平台宏。
 * 所有上层模块 (app/ 目录) 和 UI 层 (widget) 都依赖本文件，
 * 保证"时间"和"时段"两个核心概念在整个代码库中类型一致。
 *
 * 设计原则：
 *   1. 零依赖 — 只引用标准 C 头文件 <stdint.h>，不依赖任何 Qt 或平台特定库
 *   2. 纯数据 — 只包含类型定义和宏，不包含任何函数实现
 *   3. 可移植 — 更换硬件平台时只需要修改 PLATFORM_xxx 宏和 hal_datetime_t 的字段
 */

#ifndef HAL_H
#define HAL_H

#include <stdint.h>

/*===========================================================================
 * 平台识别宏
 *===========================================================================*/

/**
 * @brief 当前编译目标平台标识
 *
 * 用于条件编译，在不同平台间切换硬件相关的实现路径。
 *
 * 取值约定：
 *   PLATFORM_TINY4412 = 1  — 真实 ARM 开发板，通过 /dev/buttons 读取物理按键
 *   PLATFORM_QT       = 3  — PC 桌面模拟器，通过键盘事件模拟按键输入
 */
#define PLATFORM_QT 3

/*===========================================================================
 * 时间数据结构
 *===========================================================================*/

/**
 * @brief 统一的时间数据结构
 *
 * 封装年/月/日/时/分/秒六个时间分量，在全项目中作为时间数据的唯一载体。
 *
 * 字段类型选择：
 *   - year  用 uint16_t：支持 0~65535 年份范围，满足嵌入式设备长期使用
 *   - 其余字段用 uint8_t：各字段值域均 < 60（或 < 24），uint8_t 足够且节省内存
 *
 * 使用场景：
 *   - RTC_GetTime() 写入初始值
 *   - onTimerUpdate() 每秒累加
 *   - paintEvent() 读取并显示
 *   - KEY_Process() 在设置模式下修改
 */
typedef struct {
    uint16_t year;   /**< 年份 (1970~65535) */
    uint8_t  month;  /**< 月份 (1~12) */
    uint8_t  day;    /**< 日期 (1~31) */
    uint8_t  hour;   /**< 小时 (0~23) */
    uint8_t  minute; /**< 分钟 (0~59) */
    uint8_t  second; /**< 秒数 (0~59) */
} hal_datetime_t;

/*===========================================================================
 * 时段枚举
 *===========================================================================*/

/**
 * @brief 一天中的六个时段
 *
 * 将 24 小时划分为六个视觉上有区分度的时段，
 * 对应六种不同的太阳/月亮图标绘制方案。
 *
 * ┌──────────────────┬──────┬──────────────────┐
 * │      枚举值       │ 数值 │     覆盖时段       │
 * ├──────────────────┼──────┼──────────────────┤
 * │ ICON_MORNING_SUN │   0  │ 06:00 - 07:59   │
 * │ ICON_YELLOW_SUN  │   1  │ 08:00 - 11:59   │
 * │ ICON_GLEAMING_SUN│   2  │ 12:00 - 13:59   │
 * │ ICON_WESTERN_SUN │   3  │ 14:00 - 17:59   │
 * │ ICON_RED_SUN     │   4  │ 18:00 - 18:59   │
 * │ ICON_NIGHT_MOON  │   5  │ 19:00 - 05:59   │
 * └──────────────────┴──────┴──────────────────┘
 *
 * 使用链路：
 *   icon_get_period(hour) → icon_period_t → paintEvent() 的 switch 分支
 */
typedef enum {
    ICON_MORNING_SUN   = 0, /**< 清晨：半轮橙色朝阳从地平线升起 */
    ICON_YELLOW_SUN    = 1, /**< 早晨：天空中的纯黄圆太阳 */
    ICON_GLEAMING_SUN  = 2, /**< 中午：带八条光芒的耀眼金阳 */
    ICON_WESTERN_SUN   = 3, /**< 下午：偏向右下方的暖橙色太阳 */
    ICON_RED_SUN       = 4, /**< 傍晚：贴近海平线的深红夕阳 */
    ICON_NIGHT_MOON    = 5  /**< 夜晚：星空下的银白弯月 */
} icon_period_t;

#endif // HAL_H
